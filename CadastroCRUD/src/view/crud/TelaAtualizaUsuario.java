    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        boolean valida = true;
       
        if (txtNome.getText().isEmpty()) {
            lblNomeObr.setVisible(true);
            valida = false;
        }else{
            lblNomeObr.setVisible(false);
        }

        if (txtLogin.getText().isEmpty()) {
            lblLoginObr.setVisible(true);
            valida = false;
        }else{
            lblLoginObr.setVisible(false);
        }

        if (txtSenha.getText().isEmpty()) {
            lblSenhaObr.setVisible(true);
            valida = false;
        }else{
            lblSenhaObr.setVisible(false);
        }

        if (ftCPF.getText().trim().length() < 14) {
            lblCPFObr.setVisible(true);
            valida = false;
        }else{
            lblCPFObr.setVisible(false);
        }

        if (valida) {
            Usuario u = new Usuario();
            
            u.setNome(txtNome.getText());
            u.setLogin(txtLogin.getText());
            u.setSenha(txtSenha.getText());
            u.setCpf(ftCPF.getText());
            if (cbStatus.getSelectedItem().toString().equals("true")) {
                u.setStatus(true);
            } else {
                u.setStatus(false);
            }
            u.setDataCadastro(new Date(System.currentTimeMillis()));

            if (controller.atualizar(u, loginOriginal)) {
                JOptionPane.showMessageDialog(null, "Atualiza莽茫o realizada com sucesso!!!");
            }
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed


